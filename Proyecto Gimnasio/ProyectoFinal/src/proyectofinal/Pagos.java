    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
         cerrarArchivo();
       
         try {
         entrada= new Scanner(Paths.get("registro.txt"));
        } catch (IOException ex) {
         Logger.getLogger(Registros.class.getName()).log(Level.SEVERE, null, ex);
         }
        String nombre,apellido,direccion,telefono,contacto,codigo,genero,plan,pago;
        Double peso;
        int edad;
        String p="Diario";
        String pp="Finde";
        try{
            while(entrada.hasNext()){
                codigo=entrada.next();
                nombre=entrada.next();
                apellido=entrada.next();
                edad=entrada.nextInt();
                direccion=entrada.next();
                telefono=entrada.next();
                contacto=entrada.next();
                peso=entrada.nextDouble();
                plan=entrada.next();
                pago=entrada.next();
                genero=entrada.next();
                String c = fcod.getText();
                if(c.equals(codigo)){
                fnombre.setText(nombre);
                fapellido.setText(apellido);
                cont=cont+1;
                frecibo.setText("0"+cont);
                if(p.equals(plan)){
                 fcuota.setText("Q300.00");
                }
                if(pp.equals(plan)){
                 fcuota.setText("Q200.00");
                }
                }
            }
            
        }
        catch(NoSuchElementException elementException){
            JOptionPane.showMessageDialog(null,"ERROR");
        
        }
        
    }//GEN-LAST:event_btnbuscarActionPerformed

